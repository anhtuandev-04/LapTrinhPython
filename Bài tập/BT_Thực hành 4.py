#Bài 7.20 Insertion sort
#Viết hàm sắp xếp danh sách dùng Insertion sort, hàm trả về danh sách đã được sắp xếp.
def insertion_sort(arr):
    for i in range(1,len(arr)):
        key = arr[i]
        j = i-1
        while j>=0 and arr[j] > key:
            arr[j+1] = arr[j]
            j -= 1
        arr[j+1] = key
    return arr

