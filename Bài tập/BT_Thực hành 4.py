def cong_da_thuc(dt1, dt2):
    max_len = max(len(dt1), len(dt2))
    dt1 = [0]*(max_len-len(dt1)) + dt1
    dt2 = [0]*(max_len-len(dt2)) + dt2
    return [a+b for a,b in zip(dt1,dt2)]

def hien_thi(dt):
    s = ""
    bac = len(dt)-1
    for i, h in enumerate(dt):
        if h != 0:
            s += f"{h}x^{bac-i} + "
    return s[:-3]

