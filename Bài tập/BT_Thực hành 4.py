#Bài 7B.25 Kiểm tra số CCCD
#Viết hàm kiểm tra một số CCCD có hợp lệ hay không biết rằng cấu trúc 12 chữ số trên số căn cước công dân có ý nghĩa như sau:
#•	03 chữ số đầu tiên là mã tỉnh, thành phố trực thuộc trung ương hoặc mã quốc gia nơi công dân đăng ký khai sinh,
#•	01 chữ số tiếp theo là mã giới tính của công dân
#•	02 chữ số tiếp theo là mã năm sinh của công dân;
#•	06 chữ số cuối là khoảng số ngẫu nhiên.
#Trong đó:
#•	Mã tỉnh, thành phố trực thuộc trung ương nơi công dân khai sinh có các mã từ 001 đến 0096 tương ứng với 63 tỉnh, thành phố trong cả nước. Công dân khai sinh ở Hà Nội có mã 001, Hải Phòng có mã 031, Đà Nẵng có mã 048, TP. Hồ Chí Minh có mã 079, Phú Yên có mã 054, Quãng Ngãi có mã 051, Bình Định có mã 052, Bà Rịa – Vũng Tàu có mã 077, Long An có mã 080,…
#•	Mã thế kỷ và mã giới tính được quy ước như sau:
#o	Thế kỷ 20 (từ năm 1900 đến hết năm 1999): Nam 0, nữ 1;
#o	Thế kỷ 21 (từ năm 2000 đến hết năm 2099): Nam 2, nữ 3;
#o	Thế kỷ 22 (từ năm 2100 đến hết năm 2199): Nam 4, nữ 5;
#o	Thế kỷ 23 (từ năm 2200 đến hết năm 2299): Nam 6, nữ 7;
#o	Thế kỷ 24 (từ năm 2300 đến hết năm 2399): Nam 8, nữ 9.
#	Mã năm sinh: Thể hiện hai số cuối năm sinh của công dân.
def kiem_tra_cccd(cccd: str) -> str:
    if len(cccd) != 12 or not cccd.isdigit():
        return "❌ CCCD phải gồm đúng 12 chữ số."

    ma_tinh = int(cccd[:3])
    if not (1 <= ma_tinh <= 96):
        return f"❌ Mã tỉnh {ma_tinh:03d} không hợp lệ (chỉ từ 001–096)."

    ma_gt = int(cccd[3])
    if not (0 <= ma_gt <= 9):
        return "❌ Mã giới tính không hợp lệ."

    nam_2so = int(cccd[4:6])

    if ma_gt in [0, 1]:   # 1900–1999
        nam_sinh = 1900 + nam_2so
    elif ma_gt in [2, 3]: # 2000–2099
        nam_sinh = 2000 + nam_2so
    elif ma_gt in [4, 5]: # 2100–2199
        nam_sinh = 2100 + nam_2so
    elif ma_gt in [6, 7]:
        nam_sinh = 2200 + nam_2so
    elif ma_gt in [8, 9]:
        nam_sinh = 2300 + nam_2so
    else:
        return "❌ Mã giới tính/ thế kỷ không hợp lệ."

    gioi_tinh = "Nam" if ma_gt % 2 == 0 else "Nữ"

    so_ngau_nhien = cccd[6:]
    if not so_ngau_nhien.isdigit():
        return "❌ 6 số cuối không hợp lệ."

    return f"✅ CCCD hợp lệ!\n- Mã tỉnh: {ma_tinh:03d}\n- Giới tính: {gioi_tinh}\n- Năm sinh: {nam_sinh}\n- Số cá nhân: {so_ngau_nhien}"

if __name__ == "__main__":
    cccd = input("Nhập số CCCD: ")
    print(kiem_tra_cccd(cccd))

#Bài 7B.26 Viết hàm tạo mã căn cước công dân
#Viết hàm tạo một số CCCD với thông tin cấu trúc mã như Bài 7B.25 Kiểm tra số CCCD
#với thông tin 6 số cuối được tạo ngẫu nhiên.
import random

def tao_cccd(ma_tinh, gioi_tinh, nam_sinh):
    ma_tinh = str(ma_tinh).zfill(3)

    the_ky = nam_sinh // 100
    ma_gt = {
        19: {'nam': 0, 'nu': 1},   # 1900–1999
        20: {'nam': 2, 'nu': 3},   # 2000–2099
        21: {'nam': 4, 'nu': 5},   # 2100–2199
        22: {'nam': 6, 'nu': 7},   # 2200–2299
        23: {'nam': 8, 'nu': 9},   # 2300–2399
    }

    if the_ky not in ma_gt:
        return "❌ Năm sinh ngoài phạm vi hỗ trợ (1900–2399)."

    if gioi_tinh not in ['nam', 'nu']:
        return "❌ Giới tính phải là 'nam' hoặc 'nu'."

    gt = ma_gt[the_ky][gioi_tinh]

    ma_ns = str(nam_sinh % 100).zfill(2)

    so_ngau_nhien = str(random.randint(0, 999999)).zfill(6)

    return ma_tinh + str(gt) + ma_ns + so_ngau_nhien

if __name__ == "__main__":
    ma_tinh = int(input("Nhập mã tỉnh (001–096): "))
    gioi_tinh = input("Nhập giới tính (nam/nu): ").lower()
    nam_sinh = int(input("Nhập năm sinh (yyyy): "))

    cccd = tao_cccd(ma_tinh, gioi_tinh, nam_sinh)
    print("➡️ Mã CCCD được tạo:", cccd)