#Lưu ý: Tam giác Pascal là một hình số học và hình học do Blaise Pascal nghĩ ra đầu tiên.
def tam_giac_pascal(n):
    triangle = [[1]]
    for i in range(1, n):
        row = [1]
        for j in range(1, i):
            row.append(triangle[i-1][j-1] + triangle[i-1][j])
        row.append(1)
        triangle.append(row)
    return triangle

n = int(input("Nhập số hàng: "))
triangle = tam_giac_pascal(n)

for i in range(n):
    print(" " * (n - i), end="")
    for num in triangle[i]:
        print(num, end=" ")
    print()

