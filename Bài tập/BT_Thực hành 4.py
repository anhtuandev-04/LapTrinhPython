#Bài 7B.22 Buble sort
#Viết hàm sắp xếp danh sách dùng Buble sort, hàm trả về danh sách đã được sắp xếp.
def bubble_sort(arr):
    n = len(arr)
    for i in range(n-1):
        for j in range(n-i-1):
            if arr[j] > arr[j+1]:
                arr[j], arr[j+1] = arr[j+1], arr[j]
    return arr

