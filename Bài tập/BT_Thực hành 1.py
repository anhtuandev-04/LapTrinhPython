#Một khách hàng A, có số tiền là Y, đem gửi tiết kiệm với lãi suất mỗi năm là x %. Chu kỳ
#gửi là 06 tháng, khi tới hạn thì tài khoản tiết kiệm sẽ tất toán và gửi tất cả &lt;tiền lãi&gt;, &lt;tiền
#gốc&gt; tiếp tục vào ngân hàng với lãi suất như cũ. Biết rằng nếu người dùng rút trước hạn
#thì sẽ tính lãi suất không kỳ hạn là 1,2 % trên năm. Hãy viết chương trình cho biết người
#dùng nhận được bao nhiêu tiền sau 18 và 20 tháng với Y tiền, lãi suất x được nhập từ bàn
#phím và đối số dòng lệnh.

import sys

def main():
    LAI_SUAT_KHONG_KY_HAN_NAM = 0.012

    so_tien_goc = float(input("Nhập tiền gửi: "))
    lai_suat_hang_nam = float(input("Nhập lãi suất hàng năm (ví dụ: 0.072): "))

    print(f"\nLãi suất hàng năm là: {lai_suat_hang_nam} %")

    print("\nChu kỳ tiết kiệm thứ 01: 01 - 06")
    print(f"Tiền gốc: {so_tien_goc:.1f}")
    lai_6_thang_1 = so_tien_goc * (lai_suat_hang_nam / 12) * 6
    print(f"Tiền lãi 6 tháng đầu là: {lai_6_thang_1:.0f}")
    tien_goc_moi_1 = so_tien_goc + lai_6_thang_1
    print(f"Tiền gốc + lãi 6 tháng đầu là: {tien_goc_moi_1:.1f}")

    print("\nChu kỳ tiết kiệm thứ 02: 07 - 12")
    print(f"Tiền gốc: {tien_goc_moi_1:.0f}")
    lai_6_thang_2 = tien_goc_moi_1 * (lai_suat_hang_nam / 12) * 6
    print(f"Tiền lãi 6 tháng tiếp theo là: {lai_6_thang_2:.0f}")
    tien_goc_moi_2 = tien_goc_moi_1 + lai_6_thang_2
    print(f"Tiền gốc + lãi 6 tiếp theo là: {tien_goc_moi_2:.1f}")

    print("\nChu kỳ tiết kiệm thứ 03: 13 - 18")
    print(f"Tiền gốc: {tien_goc_moi_2:.1f}")
    lai_6_thang_3 = tien_goc_moi_2 * (lai_suat_hang_nam / 12) * 6
    print(f"Tiền lãi 6 tháng tiếp theo là: {lai_6_thang_3:.0f}")
    tien_goc_moi_3 = tien_goc_moi_2 + lai_6_thang_3
    print(f"Tiền gốc + lãi 6 tiếp theo là: {tien_goc_moi_3:.1f}")

    print("\nChu kỳ tiết kiệm thứ 04: 19 - 20")
    print(f"Lãi suất không kỳ hạn: {LAI_SUAT_KHONG_KY_HAN_NAM} trên năm")
    print(f"Tiền gốc: {tien_goc_moi_3:.1f}")

    ho_va_ten_nhap = input("\nNhập họ và tên: ")
    print(f"Họ tên: {ho_va_ten_nhap}")

    lai_2_thang_cuoi = tien_goc_moi_3 * (LAI_SUAT_KHONG_KY_HAN_NAM / 12) * 2
    print(f"Tiền lãi 2 tháng cuối: {lai_2_thang_cuoi:.0f}")

    tien_cuoi_cung = tien_goc_moi_3 + lai_2_thang_cuoi
    print(f"Tiền gốc + lãi 2 tiếp theo là: {tien_cuoi_cung:.1f}")

if __name__ == "__main__":
    main()

